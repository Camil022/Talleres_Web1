/* 1.	Consulte qué son los arrays y explique en qué se diferencian los arrays de los objetos.


 Arrays (arreglos):

Un array es una colección ordenada de elementos del mismo tipo de datos.
Los elementos de un array se almacenan en posiciones indexadas, comenzando desde el índice 0.
Pueden contener cualquier tipo de datos, incluyendo números, cadenas, booleanos, objetos, u otros arrays.
Se accede a los elementos del array mediante su índice, y se pueden modificar, agregar o eliminar elementos según sea necesario.
Se utilizan para almacenar colecciones de datos del mismo tipo, como una lista de nombres, números o cualquier otro conjunto de elementos relacionados.

Objetos:

Un objeto es una colección de pares clave-valor, donde cada valor está asociado a una clave única.
Los valores pueden ser de cualquier tipo de datos, incluyendo números, cadenas, booleanos, funciones, otros objetos, o incluso arrays.
Los objetos no tienen un orden específico para sus propiedades, lo que significa que no se puede garantizar el orden en el que se iterarán las propiedades.
Se accede a las propiedades de un objeto mediante su nombre de clave, y se pueden agregar, modificar o eliminar propiedades según sea necesario.
Se utilizan para modelar entidades con características y comportamientos, y son fundamentales para la programación orientada a objetos.
En resumen, la diferencia principal entre arrays y objetos radica en su estructura y uso: los arrays son colecciones ordenadas de elementos del mismo tipo, mientras que los objetos son colecciones de pares clave-valor que pueden contener diferentes tipos de datos y representan entidades con características y comportamientos específicos. */